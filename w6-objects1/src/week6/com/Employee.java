package week6.com;

class Employee {
	
	public String id;
	public String first_name;
	public String surname;
	public String job_title;
	
	Employee(String id,
			 String first_name,
			 String surname,
			 String job_title) {
		this.id = id;
		this.first_name = first_name;
		this.surname = surname;
		this.job_title = job_title;
	}
	
}